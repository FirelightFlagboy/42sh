#include "libft.h"
#include <stdlib.h>

char	*ft_strsub(char const *s, unsigned int start, size_t len)
{
	char	*string;

	if (!s || !(string = ft_strnew(len)))
		return (0);
	ft_strncpy(string, &s[start], len);
	return (string);
}
