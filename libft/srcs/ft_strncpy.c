#include "libft.h"

char	*ft_strncpy(char *dst, const char *src, size_t len)
{
	size_t	i;
	char	*tmp;

	i = 0;
	tmp = (char *)dst;
	while (i < len && *src)
	{
		*dst = *src;
		dst++;
		src++;
		i++;
	}
	while (i < len)
	{
		*dst = '\0';
		i++;
		dst++;
	}
	return (tmp);
}
