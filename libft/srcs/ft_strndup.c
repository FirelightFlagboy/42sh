#include "libft.h"
#include <stdlib.h>

char	*ft_strndup(char *src, size_t size)
{
	char	*str;

	if (!src || size == 0)
		return (0);
	if ((str = ft_strnew(size)))
		ft_strncpy(str, src, size);
	else
		return (0);
	return (str);
}
