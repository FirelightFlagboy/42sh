#include "libft.h"
#include <stdlib.h>

char	*ft_strdup(char *src)
{
	char	*str;

	if (!src || !(str = ft_strnew(ft_strlen(src))))
		return (NULL);
	ft_strcpy(str, src);
	return (str);
}
