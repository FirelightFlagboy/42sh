#include "sh.h"

int			ft_isescapechar(char c)
{
	return ((c == 't' || c == 'a' || c == 'b' ||
			c == 'c' || c == 'e' || c == 'f' || c == 'n'
			|| c == 'r' || c == 'v') || c == '\n');
}
